
"use strict";
$(function () {

  // ================================================================
  // ================== Helpers =====================================
  // ================================================================

  // ================================================================
  // ================== EVENTS ======================================
  // ================================================================

  // ================================================================
  // ================== FORMs =======================================
  // ================================================================
  form('#sign_in', function (f) {
    f.on_success(function (result) {
      reset_form_to_submit_more(f);
      f.find('div.buttons').after(compile_template('div.sign_in_success', {HREF: result.location}));
      erase_url_wanted();
      document.location.href = result.location;
    });
  });

}); // $(function) ======================================
