var _    = require('underscore')
, Check  = require('da_check').Check
, River  = require('da_river').River
, Topogo = require('topogo').Topogo
;

var TABLE_NAME = 'Folder';

function not_empty() {
  var args = _.toArray(arguments);
  return _.detect(args, function (v) {
    var val = (v || "").trim();
    if (val.length)
      return val;
  });
}

// ================================================================
